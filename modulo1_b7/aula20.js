// switch
// condicional com multiplos resultados

let profession = "fiscal";

switch(profession){
    case "fiscal":
        console.log("sua camisa será verde");
    break;

    case "bombeiro":
        console.log("sua camisa será vermelha");
    break;

    case "policial":
        console.log("sua camisa será azul");
    break;

    default:
        console.log("você não indicou uma profissão dentro das condicionais.");
    break;
}